                <h2 class="font-[lemon-bold] underline text-[2rem] pt-5 pb-5">Period Poverty</h2>
                <p>
                    <mark class="bg-transparent font-bold">Period Poverty</mark> is known as <mark class="bg-highlight">a lack of access to products, few water, sanitation, and hygiene (WASH) facilities, and limited menstrual health education.</mark>  Approximately 500 million females in the U.S. experience period poverty. Period poverty can manifest itself in many ways, such as struggling to afford products or not having accessibility to quality menstrual products. This in turn negatively impacts a menstruators overall menstrual health. 
                </p>
                <p class="pt-5">
                    Period poverty goes hand in hand with accessibility to products. For example, many females in rural Ethiopia do not have access to menstrual products; about 1⁄4 of rural Ethiopian females reported using materials such as paper, tissue, or cloth as alternatives to menstrual products. The same study also found that 10% of girls under 15 years old relying on makeshift menstrual products   
                </p>
                <p class="pt-5">
                    Across the world, affordability also becomes an issue. Menstruators typically spend $20 a month on period products alone, which quickly adds up. Considering that only $10 is given from food stamps toward feminine hygiene products, and many states not recognizing menstrual products as necessary, it can be difficult to afford menstrual products for those who are already in poverty. About 1 in every 5 teens in the US struggle to afford menstrual products. (Thinx, 2019)                </p>
