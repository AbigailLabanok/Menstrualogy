                <h2 class="font-[lemon-bold] underline text-[2rem] pt-5 pb-5">Stigma</h2>
                <p>
                    Especially in patriarchal societies such as India, menstruation is seen as taboo. In relation to menstrual hygiene, the lack of support for such serves to entrench the lower status of women. Many women are excluded from everyday life, such as education and workplace, while menstruating. About 70% of Indian mothers consider menstruation to be “dirty.” This belief leads to silence around the topic, which is harmful to newly menstruating girls. This stigma limits their knowledge of their hygiene and body, leading to a declining physical and mental health. In Bangladesh, only 6% of schools provide menstrual health education and ⅓ of girls report menstrual issues to negatively impact their school performance. 
